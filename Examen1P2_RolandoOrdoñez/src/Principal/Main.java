    private void LoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginMouseClicked
        // TODO add your handling code here:
        String usuario = n_usuario.getText();
        String contra = cont.getText();
        if (usuario.equals("admin")&&contra.equals("1234")&&admin.isSelected()){
            administrador A = new administrador();
            A.setVisible(true);
            this.setVisible(false);
            n_usuario.setText("");
            cont.setText("");
        }
        else{
            if (user.isSelected()){
                boolean m = false;
                for (int i = 0; i < users.size(); i++) {
                    String a = ((usuarios)users.get(i)).getUsuario();
                    String b = ((usuarios)users.get(i)).getContra();
                    if (usuario.equals(a)&&contra.equals(b)){
                        m = true;
                    }
                }
                if (m==true){
                    comun C = new comun();
                    C.setVisible(true);
                    this.setVisible(false);
                    n_usuario.setText("");
                    cont.setText("");
                }
                else{
                    JOptionPane.showMessageDialog(this, "usuario o contrase単a incorrectos");
                }
            }
            else{
                n_usuario.setText("");
                cont.setText("");
                JOptionPane.showMessageDialog(this, "usuario o contrase単a incorrecta");
            }
        }
    }//GEN-LAST:event_LoginMouseClicked

